-- ── 監査ログの「誰が」表示を架空メンバーで埋める ────────────────────────────
-- seed の insert 時は auth.uid() が null になる（SQL Editor は未認証扱いのため）。
-- トリガーが作った 'create' ログの user_id が null のままだと変更履歴の名前が空欄に
-- なるので、visits の登録者に合わせて補完する。
update demo.visit_logs l
   set user_id = v.user_id
  from demo.visits v
 where l.visit_id = v.id
   and l.user_id is null;

-- ── 変更履歴（update ログ）のデモ用に、1件だけ更新をかける ──────────────────
-- トリガーが 'update' ログを生成し、「変更前→変更後」の差分表示を確認できる。
-- 冪等: 2回目以降は where 条件に一致しないため何も起きない。
update demo.visits
   set memo = '定番の場所。安定して配れる（追記: 雨の日は人が減る）'
 where id = 'c0000000-0000-4000-8000-000000000003'
   and memo = '定番の場所。安定して配れる';

